#include <stdio.h>

int main(void)
{

	printf("Size of a char: "+sizeof(char)+" byte(s)\n");
	printf("Size of a int: "+sizeof(int)+" byte(s)\n");
	printf("Size of a long: "+sizeof(long)+" byte(s)\n");
	printf("Size of a long long int : "+sizeof(unsigned long)+" byte(s)\n");
	printf("Size of a float: "+sizeof(float)+" byte(s)\n");

	return (0);
}
